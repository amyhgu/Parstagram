@ParseClassName("Post")
public class Post extends ParseObject {
    private static final String KEY_DESCRIPTION = "description";
    private static final String KEY_IMAGE = "image";
    private static final String KEY_USER = "user";
    private static final String KEY_FAVES = "userFaves";

    public String getDescription() {
        return getString(KEY_DESCRIPTION);
    }

    public void setDescription(String description) {
        put(KEY_DESCRIPTION, description);
    }

    public ParseFile getImage() {
        return getParseFile(KEY_IMAGE);
    }

    public void setImage(ParseFile image) {
        put(KEY_IMAGE, image);
    }

    public ParseUser getUser() {
        return getParseUser(KEY_USER);
    }

    public void setUser(ParseUser user) {
        put(KEY_USER, user);
    }

    public List<ParseUser> getUserFaves() {
        return getList(KEY_FAVES);
    }

    public void setUserFaves(List users) {
        put(KEY_FAVES, users);
        saveInBackground();
    }

    public String getRelativeTimestamp() {
        PrettyTime prettyTime = new PrettyTime();
        return prettyTime.format(getCreatedAt());
    }

    public static class Query extends ParseQuery {
        public Query() {
            super (Post.class);
        }

        public Query getTop() {
            setLimit(20);
            return this;
        }

        public Query withUser() {
            include("user");
            return this;
        }
    }
}
